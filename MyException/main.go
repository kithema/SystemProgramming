package main

import "fmt"

// ageError - пользовательский тип для представления ошибки, связанной с возрастом
// Использует пустую структуру, так как не требует хранения данных
type ageError struct{}

// Error - метод для реализации интерфейса error
// Возвращает текстовое сообщение об ошибке
func (ageError) Error() string {
    return "Вам меньше 18, продажа запрещена"
}

// CanIBuyAlcohol - функция проверки возможности покупки алкоголя
// Принимает возраст в качестве параметра
// Выводит сообщение о результате проверки
func CanIBuyAlcohol(age int) {
    // Проверяем, достиг ли пользователь совершеннолетия
    if age < 18 {
        // Если возраст меньше 18, создаем экземпляр ageError
        // и выводим сообщение об ошибке
        fmt.Println(ageError{})
        return // Завершаем выполнение функции досрочно
    }
    
    // Если возраст 18 или больше, выводим сообщение об успешной покупке
    fmt.Println("Покупка успешна")
}

func main() {
    // Тестовый вызов функции с возрастом 3 года
    // Ожидаем сообщение об ошибке
    CanIBuyAlcohol(3)
    
    // Тестовый вызов функции с возрастом 33 года
    // Ожидаем сообщение об успешной покупке
    CanIBuyAlcohol(33)
}